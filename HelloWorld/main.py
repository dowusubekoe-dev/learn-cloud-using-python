# create a variable
character_name = "Derek"
character_age = "45"

# referencing variable name in print statement
print("There was once a man named " + character_name + ", ")
print("He was " + character_age + " years old. ")

# modify or assign a new value to variable
character_name = "Davon"
character_age = "70"

print("He really liked the name " + character_name + ", ")
print("but didn't like being " + character_age + ". ")

